String[] headers = {"ID", "Nombre", "Apellido", "Email", "Celular", "Clave", "Fecha Nac"};

    public void setListaUsuarios() {
        ArrayList<UsuariosModel> usuarios = datos.getUsuariosLista();
        this.jtUsuario.removeAll();
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.setColumnIdentifiers(headers);
        this.jtUsuario.setModel(tableModel);
        for (int i = 0; i < usuarios.size(); i++) {
            tableModel.addRow(usuarios.get(i).toArray());
        }
        this.jtUsuario.setModel(tableModel);
    }
