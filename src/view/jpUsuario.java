    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        String id = this.jtUsername.getText();
        String nombre = this.jtNombre.getText();
        String apellido = this.jtApellido.getText();
        String email = this.jtEmail.getText();
        String celular = this.jtCelular.getText();
        String clave = this.jtClave.getText();

        if (id.equals("") || nombre.equals("") || apellido.equals("") || email.equals("") || celular.equals("") || clave.equals("")) {
            JOptionPane.showMessageDialog(null, "Es necesario llenar Todas las casillas para poder agregar el usuario");
            //System.out.println(fec.toString());

        } else {
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            String fecha = formato.format(jdFecha.getDate());
            Long cel = Long.parseLong(celular);
            UsuariosModel usuario = new UsuariosModel(id, nombre, apellido, email, cel, clave, fecha);
            UsuariosDAO usuarioDAO = new UsuariosDAO();
            usuarioDAO.insertUsuarios(usuario);
        }
        setListaUsuarios();
    }//GEN-LAST:event_jbAgregarActionPerformed

