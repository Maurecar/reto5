    private void jbAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAgregarActionPerformed
        String resumen = this.jtResumen.getText();
        String anio = this.jtAnio.getText();
        String id = jcLibro.getSelectedItem().toString();
        String autor = jcAutor.getSelectedItem().toString();
        if (anio.equals("") || resumen.equals("") || id.equals("Escoja una opcion") || autor.equals("Escoja un Autor")) {
            JOptionPane.showMessageDialog(null, "Es necesario llenar las casillas Resumen y año,además escoger una opcion de ID libro y Autor");
        } else {
            int id_lib = Integer.parseInt(id);
            int lib_anio = Integer.parseInt(anio);
            int lib_aut = Integer.parseInt(autor);
            LibroModel libro = new LibroModel(id_lib, resumen, lib_anio, lib_aut);
            LibroDAO libroDAO = new LibroDAO();
            libroDAO.insertLibro(libro);
        }
        setListaLibros();
    }//GEN-LAST:event_jbAgregarActionPerformed

    private void jbActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbActualizarActionPerformed
        String resumen = this.jtResumen.getText();
        String anio = this.jtAnio.getText();
        String id = jcLibro.getSelectedItem().toString();
        String autor = jcAutor.getSelectedItem().toString();
        if (anio.equals("") || resumen.equals("") || id.equals("Escoja una opcion") || autor.equals("Escoja un Autor")) {
            JOptionPane.showMessageDialog(null, "Es necesario llenar las casillas Resumen y año,además escoger una opcion de ID libro y Autor");
        }else {
            int id_lib = Integer.parseInt(id);
            int lib_anio = Integer.parseInt(anio);
            int lib_aut = Integer.parseInt(autor);
            
            LibroModel libro = new LibroModel(id_lib, resumen, lib_anio, lib_aut);
            
            LibroDAO libroDAO = new LibroDAO();
            libroDAO.updateLibro(libro);
        }
        setListaLibros();
    }//GEN-LAST:event_jbActualizarActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        String id = jcLibro.getSelectedItem().toString();
        if ( id.equals("Escoja una opcion") ) {
            JOptionPane.showMessageDialog(null, "Es escoger una opcion de ID libro");
        }else{
        int id_lib = Integer.parseInt((id));

            LibroDAO libroDAO = new LibroDAO();
            libroDAO.deleteLibro(id_lib);
        }
        setListaLibros();
    }//GEN-LAST:event_jbEliminarActionPerformed


