String[] headers = {"ID", "Titulo", "Año", "Autor"};

    public void setListaLibros() {
        ArrayList<LibroModel> libros = datos.getLibrosLista();
        this.jtLibro.removeAll();
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.setColumnIdentifiers(headers);
        this.jtLibro.setModel(tableModel);
        for (int i = 0; i < libros.size(); i++) {
            tableModel.addRow(libros.get(i).toArray());
        }
        this.jtLibro.setModel(tableModel);
    }

    public void setListaIdRecursos() {
        this.IdLibrosLista = new ArrayList(datos.getIdRecursoLista());
        for (Object object : IdLibrosLista) {
            jcLibro.addItem(object.toString() + "");
        }
    }

    public void setListaIdAutor() {
        this.IdAutorLista = new ArrayList(datos.getIdAutorLista());
        for (Object object : IdAutorLista) {
            jcAutor.addItem(object.toString() + "");
        }
    }
